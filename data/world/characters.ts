function npc(cfg: Partial<CharacterInstance> & { character_id: string; name: string }): CharacterInstance {
  return {
    is_player: false,
    agent_state: 'active',
    memory_mode: 'summary',
    last_known_positions: [],
    ...cfg,
    stats: { p_level: 0, hp: 10, max_hp: 10, base_combat_power: 3, cultivation_progress: 0, ...cfg.stats },
    inventory: cfg.inventory || [],
    relationships: cfg.relationships || [],
    drives: cfg.drives || [{ type: 'maintain', description: '日常活动', priority: 0.3, progress: 0 }],
    player_conversation_memory: cfg.player_conversation_memory || [],
    short_term_buffer: { conversations: [], pending_events: [] },
    long_term_memory: { card_ids: [], total_count: 0, capacity: 50 },
    permanent_memory: cfg.permanent_memory || { core_identity: cfg.name, personality: { formality: 5, talkativeness: 5, emotional_express: 5, speech_style: '接地气', quirks: [] }, immutable_facts: [], knows_others: [] },
    faction_binding: cfg.faction_binding || { faction_id: 'FAC_外门', loyalty: 50, contribution: 0, k_level: 0 },
    position: cfg.position || { location_id: '', previous_location_id: '' },
    schedule: cfg.schedule || [{ time: '卯时', location_id: cfg.position?.location_id || '', action: '日常' }],
    identity: cfg.identity || { faction_id: 'FAC_外门', k_level: 0, title: '未知', character_type: '凡人' },
  };
}

